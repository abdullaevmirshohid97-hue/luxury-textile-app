export function getLocalizedField<T extends Record<string, unknown>>(
  item: T,
  field: string,
  language: Language
): string {
  const key = `${field}${language.charAt(0).toUpperCase() + language.slice(1)}` as keyof T;
  return (item[key] as string) || "";
}
